f1=3.14
# float to int
i1=int(f1)
print(f1)
print(i1)
print(type(i1))
print(type(f1))

# int to float
i2=100
f2=float(i2)
print(f2)
print(i2)
print(type(i2))
print(type(f2))

# string to float
i3="100"
f3=float(i3)
print(f3)
print(i3)
print(type(i3))
print(type(f3))

# int to binary
i4=10
print(bin(i4))

# int to hex
i5=10
print(hex(i5))

# int to oct
i6=10
print(oct(i6))
